								<h2>Welcome</h2>
								<p>
									The  Health Service Centre, SUST  located near the central auditorium just opposite to the basket ball ground , offers free  experienced general practitioner and emergency medical care services to all members of the University community: students (undergraduate and post graduate), teachers ,staffs and their families   in a user-friendly efficient environment. Students get medicine from the Centre at free of cost but they need to show their Medical card at all consultations and employees get medicine at minimum cost.

									The Centre provides service round the clock, seven days a week. The centre also has an ambulance.
								</p>
								<br>
								<h2>Services provided at the University Health Service Centre include:</h2>
								<ul>
									<li class="odd first-child">Routine medical examinations &amp; health checkups (blood pressure, RBS by   glucometer etc.)</li>
									<li class="even">Treatment of illness, injury, &amp; other physical problems</li>
									<li class="odd">Minor Surgical Procedures (drainage of  abscess ,boil etc)</li>
									<li class="even">Minor Wound Management</li>
									<li class="odd">Sports Injury Management </li>
									<li class="even">Travel Medicine and Advice</li>
									<li class="odd">Advice &amp; assistance for students with examination difficulties</li>
									<li class="even">Special arrangements for students with disabilities and sick students </li>
									<li class="odd">Referral to specialists ,imaging services, pathology, and physiotherapy according to need and condition of the patient </li>
									<li class="even">Organize blood grouping and donation campaign under direct supervision </li>
									<li class="odd">Vaccination :Hepatitis B and Ca cervix </li>
									<li class="even">Issuing medical card for the students and give medical clearance after completion of their respective courses(Bsc, Msc)</li>
									<li class="odd last-child">Medical Assessments/Pre-Employment Medicals :By issuing fitness  certificate</li>
								</ul>
								<br>
								<h2>Support and confidentiality</h2>
								<p>
									Our staffs are available to help you deal with your problems by treating you as a responsible adult, offering constructive support without judgment.
									<br><br>
									Consultations are held in confidence and nothing you tell a doctor will be passed on to anyone else, whether a parent, relative, friend or University staff.
								</p>
								<br>
								<h2>Our Staff:</h2>
								<p>
									Currently 7 doctors, nurse, pharmacist and supportive staffs working in different shifts.
								</p>
